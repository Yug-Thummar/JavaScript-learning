// Q4. a string a golden string if it starts with the character 'A' or 'a' and has a total length greater than equal to 5
// for a given string print if it golden or not
let string = "Apple";
if ((string[0] === "A" || string[0] === "a") && string.length>=5){
    console.log("Golden string");
}
else{
    console.log("not golden string");
}
